CREATE TABLE public.ART010 (
	ART_ID INT GENERATED BY DEFAULT AS IDENTITY
		(INCREMENT BY 1 START 1 MINVALUE 1 MAXVALUE 999999 CACHE 1),
	ART_TITU VARCHAR(50) NOT NULL,
	ART_AUTOR VARCHAR(50) NOT NULL,
	ART_IDAUTR INT,
	ART_GNR VARCHAR(30) NULL,
	ART_EDIT VARCHAR(20) NULL,

	CONSTRAINT ART010_PKEY PRIMARY KEY (ART_ID)
	CONSTRAINT ARTIDAUT FOREIGN KEY (ART_IDAUTR) REFERENCES AUT010(AUT_ID)
    );
